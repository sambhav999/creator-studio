import JSZip from "jszip";

function slugify(value) {
  return String(value || "kult-game")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "") || "kult-game";
}

function packageJsonFor(slug) {
  return JSON.stringify({
    name: slug,
    version: "1.0.0",
    private: true,
    type: "module",
    scripts: {
      dev: "vite --host 0.0.0.0",
      build: "vite build",
      preview: "vite preview"
    },
    dependencies: {
      "@vitejs/plugin-react": "^4.2.1",
      vite: "^5.0.12"
    },
    devDependencies: {}
  }, null, 2);
}

